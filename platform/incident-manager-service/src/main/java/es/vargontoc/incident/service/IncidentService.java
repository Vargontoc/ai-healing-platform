package es.vargontoc.incident.service;

import es.vargontoc.incident.domain.Incident;
import es.vargontoc.incident.repository.IncidentRepository;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import java.nio.charset.StandardCharsets;
import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;
import java.util.Optional;

@Service
public class IncidentService {

    private final IncidentRepository repository;

    public IncidentService(IncidentRepository repository) {
        this.repository = repository;
    }

    @Transactional
    public Incident reportIncident(String serviceName, String stacktrace) {
        String hash = generateHash(stacktrace);

        Optional<Incident> existingIncident = repository.findByHash(hash);

        if (existingIncident.isPresent()) {
            Incident incident = existingIncident.get();
            incident.incrementOccurrence();
            return repository.save(incident);
        } else {
            Incident newIncident = new Incident(hash, stacktrace, serviceName);
            return repository.save(newIncident);
        }
    }

    private String generateHash(String input) {
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] encodedhash = digest.digest(input.getBytes(StandardCharsets.UTF_8));
            return bytesToHex(encodedhash);
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException("SHA-256 algorithm not found", e);
        }
    }

    private String bytesToHex(byte[] hash) {
        StringBuilder hexString = new StringBuilder(2 * hash.length);
        for (int i = 0; i < hash.length; i++) {
            String hex = Integer.toHexString(0xff & hash[i]);
            if (hex.length() == 1) {
                hexString.append('0');
            }
            hexString.append(hex);
        }
        return hexString.toString();
    }
}
